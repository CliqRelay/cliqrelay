package workspaces

import (
	"net/http"

	"github.com/Authula/authula/models"

	"github.com/CliqRelay/cliqrelay/config"
	"github.com/CliqRelay/cliqrelay/interfaces"
	"github.com/CliqRelay/cliqrelay/types"
	"github.com/CliqRelay/cliqrelay/utils"
)

type CreateWorkspaceHandler struct {
	appConfig        *config.AppConfig
	workspaceService interfaces.WorkspaceService
}

func NewCreateWorkspaceHandler(appConfig *config.AppConfig, workspaceService interfaces.WorkspaceService) *CreateWorkspaceHandler {
	return &CreateWorkspaceHandler{appConfig: appConfig, workspaceService: workspaceService}
}

func (h *CreateWorkspaceHandler) Handle() http.HandlerFunc {
	return func(w http.ResponseWriter, r *http.Request) {
		ctx := r.Context()
		reqCtx, _ := models.GetRequestContext(ctx)
		actor := reqCtx.Actor

		var request types.CreateWorkspaceRequest
		if err := utils.ParseJSON(r, &request); err != nil {
			reqCtx.SetJSONResponse(http.StatusUnprocessableEntity, map[string]any{"message": err.Error()})
			reqCtx.Handled = true
			return
		}
		if err := request.Validate(); err != nil {
			reqCtx.SetJSONResponse(http.StatusUnprocessableEntity, map[string]any{"message": err.Error()})
			reqCtx.Handled = true
			return
		}

		workspace, err := h.workspaceService.Create(ctx, actor, &request)
		if err != nil {
			reqCtx.SetJSONResponse(http.StatusInternalServerError, map[string]any{"message": err.Error()})
			reqCtx.Handled = true
			return
		}

		reqCtx.SetJSONResponse(http.StatusCreated, &types.CreateWorkspaceResponse{
			Workspace: workspace,
		})
	}
}
